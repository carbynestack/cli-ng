package io.carbynestack.cli.util;

/**
 * The CLI output format.
 *
 * @since 0.5.0
 */
public enum Format {
    /**
     * Strip all formatting
     *
     * @since 0.5.0
     */
    PLAIN,
    /**
     * ANSI formatted text
     *
     * @since 0.5.0
     */
    DEFAULT,
    /**
     * JSON structure
     *
     * @since 0.5.0
     */
    JSON;
}
